def max_score_indices(nums):
    n = len(nums)
    total_ones = sum(nums)
    total_zeros = 0
    max_score = -1
    max_indices = []
    for i in range(n+1):
        if i > 0:
            if nums[i-1] == 0:
                total_zeros += 1
            else:
                total_ones -= 1
        score = total_zeros + total_ones
        if score > max_score:
            max_score = score
            max_indices = [i]
        elif score == max_score:
            max_indices.append(i)
    return max_indices
    

